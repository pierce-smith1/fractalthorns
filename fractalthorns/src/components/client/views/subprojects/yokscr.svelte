    <h1>yok.scr</h1>
    
    <img class="preview-image" src="/assets/images/yokscr/yoks.png" />
    
    <p>This is a classic OpenGL Windows screensaver with flying, screaming yonkers.
    Compatible with Windows Vista+.</p>
    
    <p><a href="https://github.com/pierce-smith1/yok.scr/releases">download</a> (Note that there is a separate version for Windows 95 and modern computers!)</p>
    
    <p>To install the screensaver, just move the <code>yok.scr</code> file into
    <code>C:\Windows</code>. It should then appear as "Yorkers" in your screensaver
    settings.</p>
    
    <p>If you get an error about <code>MSVCP140.dll</code> or
    <code>VCRUNTIME140.dll</code> or similar, you will need to install the <a
    href="https://aka.ms/vs/17/release/vc_redist.x64.exe">Microsoft Visual C++
    Redistributable</a>. (Sorry)</p>
    
    <em>For Windows Vista - get the <a
    href="https://aka.ms/vs/16/release/VC_redist.x64.exe">2015-2019
    version.</a></em>
    
    <p>Note that on Windows 10, the screensaver settings are buried in a link at
    the bottom of the Lock Screen page in Personalize.</p>
    
    <p>enjoy &lt;3</p>
